#include "ListNode.h"
class Solution
{
public:
  vector<int> reverseBookList(ListNode *head)
  {
    if (head == nullptr)
      return {};
    head = reverseList(head);
    vector<int> result;
    while (head)
    {
      result.push_back(head->val);
      head = head->next;
    }
    return result;
  }
  ListNode *reverseList(ListNode *head)
  {
    if (!head)
      return nullptr;
    ListNode *pre = nullptr;
    ListNode *cur = head;
    while (cur)
    {
      ListNode *cur_next = cur->next;
      cur->next = pre;
      pre = cur;
      cur = cur_next;
    }
    return pre;
  }
};

